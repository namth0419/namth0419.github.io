def main():
    with open(os.path.join(ROOT, "cv.json"), encoding="utf-8") as f:
        cv = json.load(f)

    html = build_html(cv)
    with open(os.path.join(ROOT, "index.html"), "w", encoding="utf-8") as f:
        f.write(html)
    print("  index.html  %6d bytes" % len(html))

    tex = build_tex(cv)
    with open(os.path.join(ROOT, "cv.tex"), "w", encoding="utf-8") as f:
        f.write(tex)
    print("  cv.tex      %6d bytes" % len(tex))

    bib = build_bib(cv)
    with open(os.path.join(ROOT, "cite.bib"), "w", encoding="utf-8") as f:
        f.write(bib)
    print("  cite.bib    %6d bytes" % len(bib))

    page404 = build_404(cv)
    with open(os.path.join(ROOT, "404.html"), "w", encoding="utf-8") as f:
        f.write(page404)
    print("  404.html    %6d bytes" % len(page404))

    robots = build_robots(cv)
    with open(os.path.join(ROOT, "robots.txt"), "w", encoding="utf-8") as f:
        f.write(robots)
    print("  robots.txt  %6d bytes" % len(robots))

    sm = build_sitemap(cv)
    if sm:
        with open(os.path.join(ROOT, "sitemap.xml"), "w", encoding="utf-8") as f:
            f.write(sm)
        print("  sitemap.xml %6d bytes" % len(sm))
    else:
        print("  ! site_url 이 비어 있어 sitemap.xml 을 건너뜁니다.")

    print("  %d publications · %d patents · %d talks · %d news"
          % (len(cv["publications"]), len(cv["patents"]), len(cv["talks"]), len(cv.get("news", []))))
    print("  last updated: %s" % resolve_updated(cv))

    if "--pdf" in sys.argv:
        for _ in range(2):
            r = subprocess.run(["pdflatex", "-interaction=nonstopmode", "cv.tex"],
                               cwd=ROOT, capture_output=True)
        if os.path.exists(os.path.join(ROOT, "cv.pdf")):
            print("  cv.pdf      %6d bytes" % os.path.getsize(os.path.join(ROOT, "cv.pdf")))
        else:
            print("  ! pdflatex failed — see cv.log")
            sys.exit(1)


if __name__ == "__main__":
    main()
