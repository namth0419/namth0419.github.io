def esc_attr(t):
    return esc_html(t).replace('"', "&quot;").replace("\n", "&#10;")


def resolve_updated(cv):
    """updated 가 'auto' 이거나 비어 있으면 마지막 커밋 날짜를 씁니다."""
    v = (cv.get("updated") or "").strip()
    if v and v.lower() != "auto":
        return v
