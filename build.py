def cite_html(p, cv):
    bits = [inline(p["authors"], "html", cv["me"]) + ". " + inline(p["title"], "html", cv["me"]) + "."]
    if p.get("venue"):
        tail = f' <span class="venue">{esc_html(p["venue"])}</span>'
        if p.get("volume"):
            tail += f', <b>{esc_html(p["volume"])}</b>'
        if p.get("pages"):
            tail += f', {esc_html(p["pages"])}'
        if p.get("year"):
            tail += f' ({esc_html(p["year"])})'
        bits.append(tail + ".")
    st = STATUS.get(p.get("status", ""))
    if st:
        bits.append(f'<span class="{st["cls"]}">{st["label"]}</span>')
    links = []
    if p.get("doi"):
        links.append(f'<a class="lnk" href="https://doi.org/{esc_html(p["doi"])}" target="_blank" rel="noopener">DOI</a>')
    if p.get("pdf"):
        links.append(f'<a class="lnk pdf" href="{esc_html(p["pdf"])}" target="_blank" rel="noopener">PDF</a>')
    links.append(f'<a class="lnk bib" href="cite.bib" data-bib="{esc_attr(bib_entry(p, cv))}">BIB</a>')
    body = "".join(bits)
    if links:
        body += '\n            <div class="links">' + "".join(links) + "</div>\n          "

    # graphical abstract → 2-column entry (웹 전용, PDF에는 안 들어감)
    img = p.get("image")
    if not img:
        return f'        <li>{body}</li>'

    alt = esc_html(p.get("image_alt") or "Graphical abstract for: " + p["title"])
    cap = p.get("image_caption", "")
    cap_html = f'\n            <span class="cap">{esc_html(cap)}</span>' if cap else ""
    return (
        '        <li class="has-fig">\n'
        '          <figure class="pub-fig">\n'
        f'            <a href="{esc_html(img)}" target="_blank" rel="noopener" '
        f'aria-label="Open full-size figure">\n'
        f'              <img src="{esc_html(img)}" alt="{alt}" loading="lazy">\n'
        f'            </a>{cap_html}\n'
        '          </figure>\n'
        f'          <div class="pub-body">{body}</div>\n'
        '        </li>'
    )


