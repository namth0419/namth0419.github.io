def jsonld(cv, site):
    here = next((i for i in cv.get("institutions", [])
                 if "current" in (i.get("years") or "").lower()), None)
    d = {
        "@context": "https://schema.org",
        "@type": "Person",
        "name": cv["me"],
        "jobTitle": cv["role_lines"][0] if cv.get("role_lines") else "",
        "email": "mailto:" + cv["personal"]["email"],
        "sameAs": [l["url"] for l in cv.get("links", [])],
    }
    if site:
        d["url"] = site + "/"
        d["image"] = site + "/assets/og.png"
    if here:
        d["affiliation"] = {"@type": "Organization", "name": here.get("full") or here["name"]}
    return ('<script type="application/ld+json">\n%s\n</script>'
            % json.dumps(d, ensure_ascii=False, indent=2))


