# ── BibTeX ───────────────────────────────────────────────────────────────────
def bib_authors(authors, me):
    """'{me}†, Seung Min Lee†, ... and Sung Gap Im*' → 'Nam, Taehyun and Lee, Seung Min and ...'"""
    s = authors.replace("{me}", me).replace("†", "").replace("*", "")
    names = []
    for chunk in s.split(","):
        for n in re.split(r"\s+and\s+", chunk):
            n = re.sub(r"^\s*and\s+", "", n).strip()
            if not n:
                continue
            w = n.split()
            names.append("%s, %s" % (w[-1], " ".join(w[:-1])) if len(w) > 1 else n)
    return " and ".join(esc_tex(n) for n in names)


def bib_key(p, me):
    first = p["authors"].replace("{me}", me).split(",")[0]
    last = re.sub(r"[^A-Za-z]", "", first.split()[-1]).lower() or "anon"
    word = ""
    for w in re.findall(r"[A-Za-z]+", p["title"]):
        if len(w) > 3 and w.lower() not in ("with", "from", "high", "that", "this", "their"):
            word = w.lower()
            break
    return "%s%s%s" % (last, p.get("year") or "", word)


BIB_NOTE = {
    "in_submission":  "Submitted to %s",
    "in_revision":    "Under revision at %s",
    "in_preparation": "Manuscript in preparation",
    "accepted":       "Accepted for publication",
}


def bib_entry(p, cv):
    published = bool(p.get("venue")) and p.get("status") in ("published", "accepted")
    fields = [("author", bib_authors(p["authors"], cv["me"])),
              ("title", "{%s}" % esc_tex(p["title"]))]

    if published:
        fields.append(("journal", esc_tex(p["venue"])))
        for k in ("volume", "year"):
            if p.get(k):
                fields.append((k, esc_tex(p[k])))
        if p.get("pages"):
            fields.append(("pages", esc_tex(p["pages"]).replace("–", "--")))
        if p.get("status") == "accepted":
            fields.append(("note", BIB_NOTE["accepted"]))
    else:
        # @unpublished 는 journal 필드를 쓰지 않습니다. 상태를 note 로 표현.
        if p.get("year"):
            fields.append(("year", esc_tex(p["year"])))
        tmpl = BIB_NOTE.get(p.get("status", ""), "Manuscript")
        note = tmpl % esc_tex(p["venue"]) if ("%s" in tmpl and p.get("venue")) else tmpl.replace(" %s", "")
        fields.append(("note", note))

    if p.get("doi"):
        fields.append(("doi", p["doi"]))

    body = ",\n".join("  %-8s = {%s}" % (k, v) for k, v in fields)
    return "@%s{%s,\n%s,\n}" % ("article" if published else "unpublished",
                                bib_key(p, cv["me"]), body)


def build_bib(cv):
    entries, keys = [], {}
    for p in cv["publications"]:
        k = bib_key(p, cv["me"])
        keys[k] = keys.get(k, 0) + 1
        entries.append(bib_entry(p, cv))
    dupes = [k for k, n in keys.items() if n > 1]
    if dupes:
        print("  ! BibTeX 키 중복: %s — cv.json 의 title/year 를 확인하세요." % ", ".join(dupes))
    head = ("%% BibTeX entries for the publications of %s\n"
            "%% Generated by build.py from cv.json — do not edit directly.\n\n" % cv["me"])
    return head + "\n\n".join(entries) + "\n"


