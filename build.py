# ── inline markup ────────────────────────────────────────────────────────────
# {me}  → own name        †  → dagger
# _x_   → emphasis        [text](url) → link
TOKEN = re.compile(r"\{me\}|†|\[([^\]]+)\]\(([^)]+)\)|(?<!\w)_([^_]+)_(?!\w)")


def esc_html(t):
    return t.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")


def esc_tex(t):
    out = []
    for ch in t:
        if ch in "&%$#_{}":
            out.append("\\" + ch)
        elif ch == "~":
            out.append("\\textasciitilde{}")
        elif ch == "^":
            out.append("\\textasciicircum{}")
        elif ch == "\\":
            out.append("\\textbackslash{}")
        else:
            out.append(ch)
    return "".join(out)


def esc_tex_url(u):
    for ch in "%#&":
        u = u.replace(ch, "\\" + ch)
    return u


def inline(text, mode, me="Taehyun Nam"):
    """Render the mini-markup for 'html' or 'tex'."""
    if text is None:
        return ""
    out, pos = [], 0
    esc = esc_html if mode == "html" else esc_tex
    for m in TOKEN.finditer(text):
        out.append(esc(text[pos:m.start()]))
        tok = m.group(0)
        if tok == "{me}":
            out.append(f'<span class="me">{esc_html(me)}</span>' if mode == "html"
                       else "\\me{%s}" % esc_tex(me))
        elif tok == "†":
            out.append("<sup>†</sup>" if mode == "html" else "\\dg")
        elif m.group(1):                                  # [text](url)
            txt, url = m.group(1), m.group(2)
            out.append(f'<a href="{esc_html(url)}" target="_blank" rel="noopener">{esc_html(txt)}</a>'
                       if mode == "html"
                       else "\\href{%s}{%s}" % (esc_tex_url(url), esc_tex(txt)))
        elif m.group(3):                                  # _emphasis_
            out.append(f"<em>{esc_html(m.group(3))}</em>" if mode == "html"
                       else "\\textit{%s}" % esc_tex(m.group(3)))
        pos = m.end()
    out.append(esc(text[pos:]))
    return "".join(out)


